#Listas o arreglos de datos
#Son utilies para almacenar mas de 1 dato en una sola variable

numeros=[1,2,3,4,5]
nombres=["Juan","Sara","Carlos","Catalina"]
print(numeros)
print(numeros[1])

#Recorrer o iterar una lista, vector o arreglo de datos
for observador in nombres:
    print(observador)

for observador in numeros:
    print(observador+10)
